	Rädern, die an verschiedenen Stellen in der Stadt abgeholt und zurückgegeben werden können.
	Das Bikesharing Konzept ist in vielen Großstädten bereits verbreitet und akzeptiert, in
	Regensburg gab es in der Verganghenheit mehrere Vorschläge einen flexiblen Radverleih
	einzuführen, bisher wurde jedoch keines der Konzepte umgesetzt, abgesehen von einem
	konventionellen Radverleih.
