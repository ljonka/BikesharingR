	</p>	
	<br />
	<p>
	Anschließend wird ein Schlüssel ausgehändigt und das dazugehörige Fahrrad kann benutzt werden. Über eine einfach zu bedienende Anwendung auf dem Smartphone wird das Fahrrad von den MitarbeiterInnen ausgetragen und bei Rücknahme wieder eingetragen.
	</p>	
	<br />
	<p>
	Die Nutzungsdauer pro Verleih sollte 24 Stunden nicht überschreiten. Die Rückgabe erfolgt bei einem unserer Bikesharingpoints, eine Auflistung dieser Stellen kann bei der Ausleihe eingesehen werden oder demnächst unter www.bikesharing-r.de.
	</p>	
	<br />
	<p>
	Das ausgeliehene Fahrrad muss sorgsam behandelt und stets gegen Diebstahl gesichert werden. Bei der Rückgabe des Rads wird dem Ausleihenden das hinterlegte Pfand wieder ausgehändigt, sofern das Rad unbeschädigt ist. Ab Januar 2015 werden die ersten Räder zum Verleih bereitstehen, die entsprechenden Bikesharingpoints werden dann unter www.bikesharing-r.de aufgelistet sein. 
	</p>
